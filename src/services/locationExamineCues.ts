export function emptySmallFindExamineCue(regionKey: string | null | undefined) {
  if (regionKey === "dream_tutorial") return null;
  if (regionKey === "riverbank" || regionKey === "willow_floodplain") {
    return "Ви придивляєтеся до мулу, очерету й камінців, але зараз берег не віддає нічого придатного. Такі місця краще перечитувати згодом: вода все пересуває.";
  }
  if (regionKey === "dry_luka") {
    return "Ви розгортаєте поглядом суху траву й дрібний хмиз. Корисне тут трапляється, але не кожного разу: сьогодні лука тримає кишені порожніми.";
  }
  if (regionKey === "chornolis_border") {
    return "Під листям і корінням нічого явного не блищить. Ліс не виглядає вичерпаним — просто не кожен уважний погляд знаходить річ.";
  }
  return "Ви уважно перевіряєте землю поруч. Нічого придатного не видно, але це радше тиша цієї миті, ніж вирок місцині.";
}
